let _snapshotCache = null;
let _snapshotLoadAttempted = false;

/**
 * Load the static score snapshot as fallback when Supabase is unavailable.
 * The snapshot is generated by src/core/snapshot_generator.py after each scoring run.
 */
async function loadSnapshot() {
  if (_snapshotCache) return _snapshotCache;
  if (_snapshotLoadAttempted) return null;
  _snapshotLoadAttempted = true;

  try {
    const res = await fetch("/data/scores-snapshot.json");
    if (res.ok) {
      _snapshotCache = await res.json();
      console.log(
        `[Snapshot] Loaded fallback: ${_snapshotCache?.products?.length || 0} products`
      );
    }
  } catch {
    // Snapshot not available — not an error, just no fallback
  }
  return _snapshotCache;
}

/**
 * Get a product from the static snapshot by slug.
 */
async function getProductFromSnapshot(slug) {
  const snapshot = await loadSnapshot();
  if (!snapshot?.products) return null;

  const product = snapshot.products.find((p) => p.slug === slug);
  if (!product || !product.scores) return null;

  // Return in a format compatible with the rest of the app
  return {
    id: product.id,
    name: product.name,
    slug: product.slug,
    url: product.url,
    type_id: product.type_id,
    logo_url: product.logo_url,
    category: product.category,
    description: null,
    safeScoring: {
      note_finale: product.scores.full?.SAFE,
      score_s: product.scores.full?.S,
      score_a: product.scores.full?.A,
      score_f: product.scores.full?.F,
      score_e: product.scores.full?.E,
      note_consumer: product.scores.consumer?.SAFE,
      note_essential: product.scores.essential?.SAFE,
    },
    evaluationDetails: product.stats
      ? {
          totalNorms: product.stats.total,
          yes: product.stats.yes,
          no: product.stats.no,
          na: product.stats.na,
          tbd: product.stats.tbd,
        }
      : null,
    _fromSnapshot: true,
    _snapshotDate: snapshot._meta?.generated_at,
  };
}

/**
 * Get products listing from the static snapshot.
 */
async function getProductsListingFromSnapshot({ limit = 50, offset = 0 } = {}) {
  const snapshot = await loadSnapshot();
  if (!snapshot?.leaderboard) return { products: [], total: 0 };

  const total = snapshot.leaderboard.length;
  const products = snapshot.leaderboard.slice(offset, offset + limit).map((p) => ({
    id: p.id,
    name: p.name,
    slug: p.slug,
    type_name: p.type_name,
    logo_url: p.logo_url,
    safeScoring: {
      note_finale: p.safe_score,
      score_s: p.scores?.S,
      score_a: p.scores?.A,
      score_f: p.scores?.F,
      score_e: p.scores?.E,
    },
    _fromSnapshot: true,
  }));

  return { products, total, _fromSnapshot: true };
}

/**
 * Fetch all evaluations for a product, bypassing Supabase 1000 row limit.
 * Uses pagination to get all results.
 *
 * @param {number} productId - Product ID
 * @returns {Object} { data: evaluations[] }
 */
async function fetchAllEvaluations(productId) {
  const allEvaluations = [];
  const batchSize = 1000;
  let offset = 0;
  let hasMore = true;

  while (hasMore) {
    const { data, error } = await supabase
      .from("evaluations")
      .select(`
        norm_id, result, why_this_result,
        norms (id, code, pillar, title)
      `)
      .eq("product_id", productId)
      .range(offset, offset + batchSize - 1);

    if (error) {
      console.error("[Supabase] Error fetching evaluations batch:", error);
      break;
    }

    if (data && data.length > 0) {
      allEvaluations.push(...data);
      offset += batchSize;
      hasMore = data.length === batchSize;
    } else {
      hasMore = false;
    }
  }

  return { data: allEvaluations };
}

